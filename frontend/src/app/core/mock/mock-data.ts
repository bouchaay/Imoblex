const makeSteps = (currentStatus: TransactionStatus, txId: string): TransactionStep[] => {
  const pipeline = [
    { s: TransactionStatus.VISIT,       label: 'Visite' },
    { s: TransactionStatus.OFFER,       label: "Offre d'achat" },
    { s: TransactionStatus.NEGOTIATION, label: 'Négociation' },
    { s: TransactionStatus.COMPROMIS,   label: 'Compromis' },
    { s: TransactionStatus.FINANCEMENT, label: 'Financement' },
    { s: TransactionStatus.ACTE,        label: 'Acte de vente' },
  ];
  const idx = pipeline.findIndex(p => p.s === currentStatus);
  return pipeline.map((p, i) => ({
    id: `${txId}-step-${i}`, transactionId: txId,
    status: p.s, label: p.label,
    isCompleted: i < idx,
    isCurrent: i === idx,
    completedAt: i < idx ? daysAgo((idx - i) * 7) : undefined,
  }));
};

